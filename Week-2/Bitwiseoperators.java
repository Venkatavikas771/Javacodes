class Bitwiseoperators
  {
    public static void main(String args[])
    {
      int a=18,b=20;
        System.out.print("Bitwise operators");
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(a<<b);
        System.out.println(a>>b);
        
     }
  }